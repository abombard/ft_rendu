char	*ft_strcat(char *dest, const char *src)
{
	int	x;
	int	y;

	x = 0;
	while (dest[x])
		x++;
	y = 0;
	while (src[y])
	{
		dest[x] = src[y];
		x++, y++;
	}
	dest[x] = '\0';
	return (dest);
}
